/*! Check single node equivalence
 *
 * @param[in]  x0   XML object 0
 * @param[in]  x1   XML object 1
 * @param[out] eq   0: equal, <0: x0 < x1, >0: x0 > x1
 * @retval     0    OK
 * @retval    -1    Error
 */
static int
xml_node_same(cxobj *x0,
              cxobj *x1,
              int   *obj)
{
    int        retval = -1; /* Not equal */
    yang_stmt *y0;
    yang_stmt *y1;
    int        ret;

    if (x0 == NULL && x1 == NULL){
        *obj = 0;
        goto ok;
    }
    else if (x0 == NULL){
        *obj = 1;
        goto ok;
    }
    else if (x1 == NULL){
        *obj = -1;
        goto ok;
    }
    if ((ret = xml_cmp(x0, x1, 0, 0, NULL)) != 0){
        *obj = ret;
        goto ok;
    }
    /* Same object */
    y0 = xml_spec(x0);
    y1 = xml_spec(x1);
    assert(y0 && y1);
    if (y0 != y1) /* choice */{
        *obj = 1;
        goto done;
    }
    *obj = 0;
 ok:
    retval = 0;
 done:
    return retval;
}

/*! Check single node equivalence
 *
 * @param[in]  x0     XML object 0
 * @param[in]  x1     XML object 1
 * @param[out] eq     0: Object is leaf and changed value, else equal or not leaf
 * @param[out] value0 If eq=0, value in x0
 * @param[out] value1 If eq=0, value in x1
 * @retval     0      OK
 * @retval    -1      Error
 */
static int
xml_node_eq(cxobj *x0,
            cxobj *x1,
            int   *eq,
            char **value0,
            char **value1)
{
    int        retval = -1; /* Not equal */
    yang_stmt *y0;
    yang_stmt *y1;
    char      *b0;
    char      *b1;

    assert(x0 && x1);
    /* Same object */
    y0 = xml_spec(x0);
    y1 = xml_spec(x1);
    assert(y0 && y1);
    if (yang_keyword_get(y0) == Y_LEAF){
        b0 = xml_body(x0);
        b1 = xml_body(x1);
        if (b0 == NULL && b1 == NULL)
            *eq = 1;
        else if (b0 == NULL || b1 == NULL
                 || strcmp(b0, b1) != 0
                 ){
            *eq = 0;
            if (value0)
                *value0 = b0;
            if (value1)
                *value1 = b1;
        }
        else
            *eq = 1;
    }
    else
        *eq = 1;
    retval = 0;
    // done:
    return retval;
}

/*! Check if conflict when rebasing tree x0 to x2 for x1
 *
 * x0 is original base candidate datastore
 * x1 is modified candidate datastore
 * x2 Current datastore, the new base
 * This function checks issues with replacing the candidate datastore (x1) with the
 * current running config (x2) and replaying the the config changes (x0->x1)
 * against it.
 * Number of conflicts are returned, and the nature of each conflict is logged with
 * debug flag xml (start with -D xml)
                           update commit
          +--------------------+---+------> private candidate 1
         /                  x1 ^    \
     x0 /  edit-config     x2 /      âŒ„
   +---+--------+--------+---+-------+----> running configuration
    edit-config  \       ^
                  \     /
                   +---+------------------> private candidate 2
                    commit

 * @param[in]  h        Clixon handle
 * @param[in]  x0       Original
 * @param[in]  x1       Candidate
 * @param[in]  x2       Running
 * @param[out] conflict Set to number of conflicts, if any
 * @retval     0        OK
 * @retval    -1        Error
 * @see xml_tree_equal
 * @see xml_diff
 */
int
xml_rebase_check(clixon_handle h,
                 cxobj        *x0,
                 cxobj        *x1,
                 cxobj        *x2,
                 int          *conflict)
{
    int    retval = -1;
    cxobj *x0c;
    cxobj *x1c;
    cxobj *x2c;
    int    same10;
    int    same20;
    int    same12;
    char  *xpath0 = NULL;
    char  *xpath1 = NULL;
    char  *xpath2 = NULL;
    int    eq1;
    int    eq2;
    char  *value0;
    char  *value1;

    x0c = x1c = x2c = NULL;
    x0c = xml_child_each(x0, x0c, CX_ELMNT);
    x1c = xml_child_each(x1, x1c, CX_ELMNT);
    x2c = xml_child_each(x2, x2c, CX_ELMNT);
    while (x0c != NULL && x1c != NULL && x2c != NULL){
        if (x0c && xml2xpath(x0c, NULL, 0, 0, &xpath0) < 0)
            goto done;
        if (x1c && xml2xpath(x1c, NULL, 0, 0, &xpath1) < 0)
            goto done;
        if (x2c && xml2xpath(x2c, NULL, 0, 0, &xpath2) < 0)
            goto done;
        if (xml_node_same(x1c, x0c, &same10) < 0)
            goto done;
        if (xml_node_same(x2c, x0c, &same20) < 0)
            goto done;
        if (xml_node_same(x1c, x2c, &same12) < 0)
            goto done;
        clixon_debug(CLIXON_DBG_XML, "%d %d %d\n", same10, same20, same12);
        eq1 = 0;
        if (same10 < 0){ /* New in x1c */
            if (same20 < 0){
                if (same12 == 0){
                    if (xml_tree_equal(x1c, x2c) != 0){
                        clixon_debug(CLIXON_DBG_XML, "Conflict %d: x0:%s x1:%s x2:%s Both added unequal object",
                                (*conflict+1), xpath0, xpath1, xpath2);
                        (*conflict)++;
                    }
                }
                x2c = xml_child_each(x2, x2c, CX_ELMNT);
            }
            x1c = xml_child_each(x1, x1c, CX_ELMNT);
            goto next;
        }
        else if (same10 == 0){
            if (same20 < 0){
                x1c = xml_child_each(x1, x1c, CX_ELMNT);
                goto next;
            }
            else if (same20 == 0){
                /* Same node, has value changed? */
                eq1 = eq2 = 0;
                if (xml_node_eq(x1c, x0c, &eq1, &value1, &value0) < 0)
                    goto done;
                if (eq1 == 0){
                    if (xml_node_eq(x2c, x0c, &eq2, NULL, NULL) < 0)
                        goto done;
                    if (eq2 == 0){
                        clixon_debug(CLIXON_DBG_XML, "Conflict %d: x0:%s x1:%s x2:%s x1 and x2 both changed value\n",
                                (*conflict+1), xpath0, xpath1, xpath2);
                        (*conflict)++;
                    }
                }

            }
            else if (same20 > 0){
                /* x2c deleted -> Conflict if to x1-x0 */
                if (xml_tree_equal(x0c, x1c) != 0){
                    clixon_debug(CLIXON_DBG_XML, "Conflict %d: x0:%s x1:%s x2:%s x2c removed",
                            (*conflict+1), xpath0, xpath1, xpath2);
                    (*conflict)++;
                }
                x0c = xml_child_each(x0, x0c, CX_ELMNT);
                x1c = xml_child_each(x1, x1c, CX_ELMNT);
                goto next;
            }
        }
        else if (same10 > 0){ /* Deleted x1c */
            if (same20 < 0){
                x2c = xml_child_each(x2, x2c, CX_ELMNT);
                goto next;
            }
            else if (same20 == 0){
                if (xml_tree_equal(x0c, x2c) != 0){
                    clixon_debug(CLIXON_DBG_XML, "Conflict %d: x0:%s x1:%s x2:%s x1c removed",
                            (*conflict+1), xpath0, xpath1, xpath2);
                    (*conflict)++;
                }
                x0c = xml_child_each(x0, x0c, CX_ELMNT);
                x2c = xml_child_each(x2, x2c, CX_ELMNT);
                goto next;
            }
        }
        assert(same10 == same20 && same10 == same12);
        if (xml_rebase_check(h, x0c, x1c, x2c, conflict) < 0)
            goto done;
        x0c = xml_child_each(x0, x0c, CX_ELMNT);
        x1c = xml_child_each(x1, x1c, CX_ELMNT);
        x2c = xml_child_each(x2, x2c, CX_ELMNT);
    next:
        if (xpath0){
            free(xpath0);
            xpath0 = NULL;
        }
        if (xpath1){
            free(xpath1);
            xpath1 = NULL;
        }
        if (xpath2){
            free(xpath2);
            xpath2 = NULL;
        }
    }
    retval = 0;
 done:
    if (xpath0)
        free(xpath0);
    if (xpath1)
        free(xpath1);
    if (xpath2)
        free(xpath2);
    return retval;
}
